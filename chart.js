    static judge(note, offset) {
        offset = Math.abs(offset);
        if (offset > 160) {
            return 0;
        }
        else {
            if (note.type === note_type.hold) { // more lenient
                if (offset > 120)
                    return 2;
                if (offset > 80)
                    return 3;
                return 4;
            }
            else {
                if (offset > 120)
                    return 1;
                if (offset > 80)
                    return 2;
                if (offset > 40)
                    return 3;
                return 4;
            }
        }
    }
